"""
Инкремент - оператор, который увеличивает 
значение переменной и сохраняет в ней новое 

var = var + значение
var += значение 
На месте плюса может стоять любой математический оператор 

Декремент - оператор, который уменьшает
значение переменной и сохраняет в ней новое

var = var - значение 
var -= значение 
На месте минуса может стоять любой математический оператор 
"""
